// Case 1: 80 calls
let calls_1 = 80;
let bill_1 = 0;

if (calls_1 <= 100) {
  bill_1 = 0;
} else {
  bill_1 = (calls_1 - 100) * 1;
}
console.log("Number of calls: " + calls_1);
console.log("Total Bill: " + bill_1 + " Rs");
console.log("---------------------------");

